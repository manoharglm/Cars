export let _storeData = async (data,id) => {
    if (data) {
        try {
            await AsyncStorage.setItem(id, data)
        } catch (error) {
            console.log(error);
        }
    }
}

export let _retrieveData = async (id) => {
    try {
        const value = await AsyncStorage.getItem(id);
        return JSON.parse(value)
    } catch (error) {
        console.log(error);
    }
}